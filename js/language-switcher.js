// Language switcher functionality
document.addEventListener('DOMContentLoaded', function() {
    const languageSwitcher = document.getElementById('language-switcher');
    
    if (languageSwitcher) {
        // Set initial language based on browser preference or default to English
        const userLang = navigator.language || navigator.userLanguage; 
        const defaultLang = userLang.startsWith('fr') ? 'fr' : userLang.startsWith('ar') ? 'ar' : 'en';
        
        languageSwitcher.value = localStorage.getItem('preferredLang') || defaultLang;
        updatePageLanguage(languageSwitcher.value);
        
        languageSwitcher.addEventListener('change', function() {
            const selectedLang = this.value;
            localStorage.setItem('preferredLang', selectedLang);
            updatePageLanguage(selectedLang);
            
            // Update text direction for Arabic
            if (selectedLang === 'ar') {
                document.documentElement.dir = 'rtl';
                document.documentElement.lang = 'ar';
            } else {
                document.documentElement.dir = 'ltr';
                document.documentElement.lang = selectedLang;
            }
        });
    }
});

function updatePageLanguage(lang) {
    const elements = document.querySelectorAll('[data-i18n]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang] && translations[lang][key]) {
            element.textContent = translations[lang][key];
        }
    });
}